// Вы вводите значение с клавиатуры. 
// Если значение равно 0, то вывести ‘Ноль’, 
// если < 0, то вывести ‘Отрицательное’, если > 0, 
// то вывести ‘Положительное’

const n = 5;

if (n == 0) {
    console.log('Ноль');
} else if (n < 0) {
    console.log('Отрицателное');
} else if (n > 0) {
    console.log('Положительное');
}